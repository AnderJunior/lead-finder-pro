-- Função para verificar se é super_admin
CREATE OR REPLACE FUNCTION public.is_super_admin()
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.users
     WHERE auth_id = auth.uid()
       AND role = 'super_admin'
  );
$$;

-- RLS para planos (super_admin pode tudo, outros só leem ativos)
ALTER TABLE public.planos ENABLE ROW LEVEL SECURITY;

CREATE POLICY "planos_select_all" ON public.planos
  FOR SELECT USING (true);

CREATE POLICY "planos_manage_super_admin" ON public.planos
  FOR ALL USING (public.is_super_admin());

-- RLS para assinaturas
ALTER TABLE public.assinaturas ENABLE ROW LEVEL SECURITY;

CREATE POLICY "assinaturas_select_own" ON public.assinaturas
  FOR SELECT USING (
    empresa_id = public.get_my_empresa_id()
    OR public.is_super_admin()
  );

CREATE POLICY "assinaturas_manage_super_admin" ON public.assinaturas
  FOR ALL USING (public.is_super_admin());

-- RLS para pagamentos
ALTER TABLE public.pagamentos ENABLE ROW LEVEL SECURITY;

CREATE POLICY "pagamentos_select_own" ON public.pagamentos
  FOR SELECT USING (
    empresa_id = public.get_my_empresa_id()
    OR public.is_super_admin()
  );

CREATE POLICY "pagamentos_manage_super_admin" ON public.pagamentos
  FOR ALL USING (public.is_super_admin());
