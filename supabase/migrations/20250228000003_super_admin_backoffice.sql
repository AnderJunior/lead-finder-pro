-- ================================================================
-- Super Admin Backoffice: planos, assinaturas, pagamentos
-- ================================================================

-- Tabela de planos do sistema
CREATE TABLE IF NOT EXISTS public.planos (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome text NOT NULL,
  descricao text,
  preco_mensal numeric(10,2) NOT NULL DEFAULT 0,
  preco_anual numeric(10,2) NOT NULL DEFAULT 0,
  max_usuarios integer NOT NULL DEFAULT 5,
  max_leads integer NOT NULL DEFAULT 1000,
  max_buscas_mes integer NOT NULL DEFAULT 100,
  recursos text[] DEFAULT '{}',
  ativo boolean NOT NULL DEFAULT true,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- Tabela de assinaturas (uma por empresa)
CREATE TABLE IF NOT EXISTS public.assinaturas (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  empresa_id bigint NOT NULL REFERENCES public.configuracoes_empresa(id) ON DELETE CASCADE,
  plano_id bigint NOT NULL REFERENCES public.planos(id),
  status text NOT NULL DEFAULT 'ativa' CHECK (status IN ('ativa','vencida','cancelada','trial','suspensa')),
  ciclo text NOT NULL DEFAULT 'mensal' CHECK (ciclo IN ('mensal','anual')),
  valor numeric(10,2) NOT NULL DEFAULT 0,
  data_inicio timestamptz NOT NULL DEFAULT now(),
  data_vencimento timestamptz NOT NULL,
  data_cancelamento timestamptz,
  observacoes text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- Tabela de pagamentos
CREATE TABLE IF NOT EXISTS public.pagamentos (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  assinatura_id bigint NOT NULL REFERENCES public.assinaturas(id) ON DELETE CASCADE,
  empresa_id bigint NOT NULL REFERENCES public.configuracoes_empresa(id) ON DELETE CASCADE,
  valor numeric(10,2) NOT NULL,
  status text NOT NULL DEFAULT 'pendente' CHECK (status IN ('pendente','pago','atrasado','cancelado')),
  data_vencimento timestamptz NOT NULL,
  data_pagamento timestamptz,
  metodo_pagamento text,
  referencia text,
  observacoes text,
  created_at timestamptz DEFAULT now()
);

